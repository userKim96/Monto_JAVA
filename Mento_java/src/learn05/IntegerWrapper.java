package learn05;

public class IntegerWrapper {
	public static void main(String[] args) {
		
		Integer ii = 30;
		//Intger > int
		System.out.println(ii.intValue()+3);
		//Intger > doble
		System.out.println(ii.doubleValue());
		//Intger > float
		System.out.println(ii.floatValue()+3);
		//Intger > String
		System.out.println(ii.toString()+3);
	}
	

}
